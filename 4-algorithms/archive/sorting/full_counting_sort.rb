def full_counting_sort_prep(array)
  counter = Array.new(100, 0)

  array.each do |mixed|
    number = mixed.to_i
    counter[number] += 1
  end

  result = []
  counter.inject(0) do |sum, count|
    total = sum + count
    result << total
    total
  end
  result
end

